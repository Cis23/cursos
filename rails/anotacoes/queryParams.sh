#params = são parametros enviador ao controller através da url
#Para utilizar, basta colocar, logo após a URL padrão, o símbolo de interrogação "?" seguido de um par chave=valor, o valor será oq precisamos que seja enviado para o servidor.
# Exemplo:
    http://localhost:3000/welcome/index?nome=valor

#Podemos também passar mais de 1 parâmetro usando o & ("E" comercial)
# Exemplo: 
    http://localhost:3000/welcome/index?nome=Francisco&sobrenome=Cavalcante 